    private void btnSmpnTokoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSmpnTokoActionPerformed
        try {
        if(txtIdToko.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "ID Toko Belum Diisi");
            txtIdToko.requestFocus();
        } else if(txtNamaToko.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama Toko Belum Diisi");
            txtNamaToko.requestFocus();
        } else if(txtAlamatToko.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Alamat Toko Belum Diisi");
            txtAlamatToko.requestFocus();
        } else if(txtTlpToko.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No Telp Toko Belum Diisi");
            txtTlpToko.requestFocus();
        } else if(txtRatingToko.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Rating Belum Diisi");
            txtRatingToko.requestFocus();
        } else if(txtProdukRelasi.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Produk Belum Diisi");
            txtProdukRelasi.requestFocus();
        
        } else {
            mycode.simpanToko02(txtIdToko.getText(), txtNamaToko.getText(),
                                txtAlamatToko.getText(), txtTlpToko.getText(),
                                txtRatingToko.getText(), txtProdukRelasi.getText());

            // Refresh tabel
            mycode.tampilDataToko(tabelToko, "SELECT*FROM toko");

            // Cek duplikasi
            if (mycode.duplikasi == true){
                txtNamaToko.setText(mycode.CEK_NAMA_TOKO);
                txtAlamatToko.setText(mycode.CEK_ALAMAT_TOKO);
                txtTlpToko.setText(mycode.CEK_NO_TLP_TOKO);
                txtRatingToko.setText(mycode.CEK_RATING);
                txtProdukRelasi.setText(mycode.CEK_PRODUK_TOKO);
               
            }
        }
    } catch (Exception e) {
    }
    }//GEN-LAST:event_btnSmpnTokoActionPerformed

