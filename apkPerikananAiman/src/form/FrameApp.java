    private void btnSmpnProdukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSmpnProdukActionPerformed
        try {
        if(txtIdProduk.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "ID Belum Diisi");
            txtIdProduk.requestFocus();
        } else if(txtNamaProduk.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama Produk Belum Diisi");
            txtNamaProduk.requestFocus();
        } else if(txtDeskripsi.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Deskripsi Belum Diisi");
            txtDeskripsi.requestFocus();
        } else if(txtKategori.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Kategori Belum Diisi");
            txtKategori.requestFocus();
        } else if(txtHrgProduk.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Harga Belum Diisi");
            txtHrgProduk.requestFocus();
        } else if(txtBerat.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Berat Belum Diisi");
            txtBerat.requestFocus();
        } else if(txtStok.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Stok Belum Diisi");
            txtStok.requestFocus();
        // Validasi txtGambarProduk DIHILANGKAN
        } else {
            mycode.simpanProduk02(txtIdProduk.getText(), txtNamaProduk.getText(),
                                  txtDeskripsi.getText(), txtKategori.getText(),
                                  txtHrgProduk.getText(), txtBerat.getText(),
                                  txtStok.getText());

            // Refresh tabel
            mycode.tampilDataProduk(tabelProduk, "SELECT*FROM produk");

            // Cek duplikasi
            if (mycode.duplikasi == true){
                txtNamaProduk.setText(mycode.CEK_NAMA_PRODUK);
                txtDeskripsi.setText(mycode.CEK_DESKRIPSI);
                txtKategori.setText(mycode.CEK_KATEGORI);
                txtHargaTrans.setText(mycode.CEK_HARGA);
                txtBerat.setText(mycode.CEK_BERAT);
                txtStok.setText(mycode.CEK_STOK);
                // Pengisian txtGambarProduk DIHILANGKAN
            }
        }
    } catch (Exception e) {
    }

    }//GEN-LAST:event_btnSmpnProdukActionPerformed

